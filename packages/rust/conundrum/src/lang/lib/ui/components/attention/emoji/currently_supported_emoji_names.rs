impl CURRENTLY_SUPPORTED_EMOJI_NAMES {
    #[cfg(feature = "wasm")]
    pub fn search_name(query: &str) -> Vec<String> {
        CURRENTLY_SUPPORTED_EMOJI_NAMES.iter()
                                       .filter_map(|f| {
                                           if f.contains(query) {
                                               Some(f.clone())
                                           } else {
                                               None
                                           }
                                       })
                                       .collect::<Vec<String>>()
    }

    #[cfg(not(feature = "wasm"))]
    pub fn search_name(query: &str) -> Vec<String> {
        CURRENTLY_SUPPORTED_EMOJI_NAMES.par_iter()
                                       .filter_map(|f| {
                                           if f.contains(query) {
                                               Some(f.clone())
                                           } else {
                                               None
                                           }
                                       })
                                       .collect::<Vec<String>>()
    }
}

