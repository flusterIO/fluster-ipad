use crate::lang::runtime::{state::conundrum_error_variant::ConundrumModalResult, traits::conundrum_input::ArcState};

/// For users that want to export their notes to other platforms, or just the
/// comfort of know that their notes are not locked in to Conundrum syntax
/// forever, there's a markdown target.
///
/// Each component will have it's own markdown rendering template, with options
/// configurable both on the component level and on the document level via
/// [flags](crate::lang::runtime::state::parse_state::ConundrumModifier.
///
/// Of course you'll lose some of the sweet styling and layout that Conundrum
/// gives you, but you won't lose any data that can possibly be converted to
/// markdown.
pub trait MarkdownComponentResult {
    /// For backwards compatability each Conundrum component has it's own
    /// Markdown syntax equivalent that can be generated by the transpiler.
    /// Just provide a `.preferMarkdownSyntax`
    /// [flag](crate::lang::runtime::state::parse_state::ConundrumModifier) to
    /// the transpiler to output regular commonmark
    /// compatible markdown.
    fn to_markdown(&self, res: ArcState) -> ConundrumModalResult<String>;
}
